    private void btoNuevaOrdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoNuevaOrdActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)jTableOrden.getModel();

        for(int i = jTableOrden.getRowCount()-1; i >= 0; i--){
            tblModel.removeRow(i);
        }

        textNomC.setEnabled(true);
        textNumO.setEnabled(true);
        btoConfInfo.setEnabled(true);
        jLabelNom.setText("");
        jLabelNum.setText("");
    }//GEN-LAST:event_btoNuevaOrdActionPerformed

