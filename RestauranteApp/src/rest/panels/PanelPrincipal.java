    private void btoConfirmarMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoConfirmarMActionPerformed
        btoNuevaOrd.setEnabled(true);
        if(jCheckBeefB.isSelected()){
            String data[] = {"Beff Burger","1", "1.50"};
            DefaultTableModel tblModel = (DefaultTableModel)jTableOrden.getModel();
            tblModel.addRow(data);
            
            activarMenu(false);
        
        }if(jCheckCheseB.isSelected()){
            String data[] = {"Chese Burger","1", "2.00"};
            DefaultTableModel tblModel = (DefaultTableModel)jTableOrden.getModel();
            tblModel.addRow(data);
            
            activarMenu(false);
        
        }if(jCheckSalmonB.isSelected()){
            String data[] = {"Salmon Burger","1", "1.70"};
            DefaultTableModel tblModel = (DefaultTableModel)jTableOrden.getModel();
            tblModel.addRow(data);
            
            activarMenu(false);
        
        }if(jCheckGrillB.isSelected()){
            String data[] = {"Grill Burger","1", "2.50"};
            DefaultTableModel tblModel = (DefaultTableModel)jTableOrden.getModel();
            tblModel.addRow(data);
            
            activarMenu(false);
        
            
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione algo del Menú");
            btoNuevaOrd.setEnabled(false);
        }
    }//GEN-LAST:event_btoConfirmarMActionPerformed

